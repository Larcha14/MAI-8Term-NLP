TEST_CASES = [
    ("T1", "Клиент 34 года, женат, высшее образование, доход 120000, работает 5 лет, просрочек не было"),
    ("T2", "Клиент 29 лет, не женат, высшее образование, доход 65000, работает 2 года, просрочек не было"),
    ("T3", "Клиент 22 года, не женат, без высшего образования, доход 35000, работает 0.5 года, просрочки были"),
    ("T4", "Клиент 53 года, женат, высшее образование, доход 90000, работает 20 лет, просрочек не было"),
    ("T5", "Клиент 28 лет, не женат, высшее образование, доход 70000, работает 1 год, просрочек не было"),
    ("T6", "Клиент 34 года, женат, доход 120000, работает 5 лет, просрочек не было"),
    ("T7", "Клиент женат, работает давно"),
    ("T8", "Ну короче мужику 34, женатый, вышка есть, получает где-то 120к, на работе уже лет 5, долгов по кредитам вроде не было"),
    ("T9", "Привет! Расскажи, что ты умеешь."),
    ("T10", "Как обычно оценивают кредитоспособность клиента?"),
    ("T11", "Как дела?"),
    ("T12", "абырвалг лалала 777 женат картошка синяя"),
]

for test_id, message in TEST_CASES:
    print(f"\n=== {test_id} ===")
    print("INPUT:", message)
    try:
        r = requests.post(API_URL, json={"message": message}, timeout=120)
        print("STATUS:", r.status_code)
        print("OUTPUT:", r.text)
    except Exception as e:
        print("ERROR:", e)